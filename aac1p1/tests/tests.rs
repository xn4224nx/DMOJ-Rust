use assert_cmd::{Command, pkg_name};

#[test]
fn full_program_exp0() {
    let mut cmd = Command::cargo_bin(pkg_name!()).unwrap();
    let assert = cmd.write_stdin(concat!("6 2\n",)).assert();
    assert.success().stdout(concat!("SQUARE\n",));
}

#[test]
fn full_program_exp1() {
    let mut cmd = Command::cargo_bin(pkg_name!()).unwrap();
    let assert = cmd.write_stdin(concat!("12 124\n",)).assert();
    assert.success().stdout(concat!("CIRCLE\n",));
}
